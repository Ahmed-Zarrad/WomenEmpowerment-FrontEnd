
export class JobOffre {
   offreId: number;
   titleOffre: string ;
   salary: number ;
  offreDescription: string;

  constructor(offreId: number, titleOffre: string, salary: number, offreDescription: string)
  {
    this.offreId = offreId;
    this.titleOffre = titleOffre;
    this.titleOffre = titleOffre;
    this.salary = salary;
    this.offreDescription = offreDescription;

  }
}
